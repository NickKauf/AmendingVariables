public class AmendingVariables {
    public static void main(String[] args)
    {
       int year = 1;
       double income = 62000;
       year++;
       income = income*1.1;
       year++;
       income = income*1.1;
       year++;
       income = income*1.1;
       year++;
       income = income*1.1;
       System.out.println("Year " + year + " income: " + income);



    }
}
